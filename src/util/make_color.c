#include "minirt.h"

int	make_color(double r, double g, double b)
{
	int	l_r;
	int	l_g;
	int	l_b;

	l_r = 255.999 * r;
	l_g = 255.999 * g;
	l_b = 255.999 * b;
	return (l_r << 16 | l_g << 8 | l_b);
}
